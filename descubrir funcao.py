

# descobrir comportamento de var 

var=input('informe o valor das senas ==> ')

print('O seu tipo Primitivo é',type(var))

print('E capitalizando',var.istitle())

print('Tem espaço',var.isspace())
print('E alfabeto',var.isalpha())

print(' é Numero'+var.isnumeric())
print('é Alfa Numerico'+var.isalnum())

print('esta em Maiusculo',var.isupper())
print('esta em Minusculo',var.islower())


